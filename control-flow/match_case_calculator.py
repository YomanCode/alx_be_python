num1 = int(input("Enter the first number: "))
num2 = int(input("Enter the second number: "))

op = input("Choose the operation (+,-,*,/")

if op == "+":
    print("The result is ",num1 + num2)
elif op == "-":
    print("The result is ", num1 - num2)
elif op == "*":
    print("The result is", num1 * num2)
elif op == "/" & num2 == 0:
    print("Can't devide by 0")
elif op == "/":
    print("The result is", num1 / num2)
